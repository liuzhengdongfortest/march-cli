import { strict as assert } from "node:assert";
import { readFileSync } from "node:fs";
import { join } from "node:path";
import { resolveStaticPath } from "../src/web-ui/server.mjs";

export async function runWebUiSmoke({ cwd = process.cwd() } = {}) {
  console.log("--- smoke: web UI prototype ---");
  const root = join(cwd, "src", "web-ui");
  const app = readFileSync(join(root, "src", "App.tsx"), "utf8");
  const shell = readFileSync(join(root, "src", "components", "AppShell.tsx"), "utf8");
  const explorer = readFileSync(join(root, "src", "components", "FileExplorer.tsx"), "utf8");
  const treeAdapter = readFileSync(join(root, "src", "fileTreeAdapter.ts"), "utf8");
  const timeline = readFileSync(join(root, "src", "components", "SessionTimeline.tsx"), "utf8");
  const timelineList = readFileSync(join(root, "src", "components", "timeline", "TimelineList.tsx"), "utf8");
  const timelineBlocks = readFileSync(join(root, "src", "components", "timeline", "TimelineBlocks.tsx"), "utf8");
  const timelineAdapter = readFileSync(join(root, "src", "timelineAdapter.ts"), "utf8");
  const right = readFileSync(join(root, "src", "components", "RightSidebar.tsx"), "utf8");
  const composer = readFileSync(join(root, "src", "components", "Composer.tsx"), "utf8");
  const model = readFileSync(join(root, "src", "model.ts"), "utf8");
  const runtimeClient = readFileSync(join(root, "src", "runtime", "client.ts"), "utf8");
  const runtimeHook = readFileSync(join(root, "src", "runtime", "useWebRuntime.ts"), "utf8");
  const runtimeTimeline = readFileSync(join(root, "src", "runtime", "runtimeTimeline.ts"), "utf8");
  const css = readFileSync(join(root, "src", "styles", "shell.css"), "utf8");
  const tokens = readFileSync(join(root, "src", "styles", "tokens.css"), "utf8");
  const pkg = readFileSync(join(cwd, "package.json"), "utf8");
  const args = readFileSync(join(cwd, "src", "cli", "args.mjs"), "utf8");
  const configuredCommand = readFileSync(join(cwd, "src", "cli", "startup", "configured-command.mjs"), "utf8");
  const server = readFileSync(join(root, "server.mjs"), "utf8");
  const webCommand = readFileSync(join(root, "command.mjs"), "utf8");
  const runtimeHost = readFileSync(join(root, "runtime-host.mjs"), "utf8");

  assert.match(pkg, /"web:dev": "vite --config src\/web-ui\/vite\.config\.mjs"/);
  assert.match(pkg, /"web:build": "tsc -p src\/web-ui\/tsconfig\.json/);
  assert.match(pkg, /"@pierre\/trees":/);
  assert.match(args, /"web"/);
  assert.match(configuredCommand, /runWebUiCommand/);
  assert.match(webCommand, /createWebRuntimeHost/);
  assert.match(webCommand, /Web UI build not found/);
  assert.match(runtimeHost, /createRuntimeRunner/);
  assert.match(runtimeHost, /prepareTurnInput/);
  assert.match(runtimeHost, /runtimeUiEvents\.emit\(\{ type: "web_user_message"/);
  assert.match(server, /\/api\/snapshot/);
  assert.match(server, /\/api\/events/);
  assert.match(server, /\/api\/turn/);
  assert.match(app, /useWebRuntime/);
  assert.match(shell, /<FileExplorer root=\{model\.workspace\}/);
  assert.match(shell, /<SessionTimeline timeline=\{model\.timeline\}/);
  assert.match(shell, /<RightSidebar sessions=\{model\.sessions\} activity=\{model\.activity\}/);
  assert.match(model, /export type WebUiModel/);
  assert.match(model, /MarchTimelineEvent/);
  assert.match(model, /TimelineItem/);
  assert.match(model, /ActivityEvent/);
  assert.match(explorer, /@pierre\/trees\/react/);
  assert.match(explorer, /aria-label="Projects"/);
  assert.match(explorer, /className="projects-header"/);
  assert.match(explorer, /className="project-tree-host"/);
  assert.match(treeAdapter, /createProjectFileTreeInput/);
  assert.match(treeAdapter, /GitStatusEntry/);
  assert.match(timeline, /normalizeTimelineEvents/);
  assert.match(timeline, /runtime-pill/);
  assert.match(timeline, /<TimelineList items=\{items\}/);
  assert.match(timelineList, /aria-label="Session events"/);
  assert.match(timelineBlocks, /className="timeline-aux tool-block"/);
  assert.match(timelineBlocks, /permission-block/);
  assert.match(timelineAdapter, /tool_result/);
  assert.match(right, /className="right-header">会话/);
  assert.match(right, /right-divider">Activity/);
  assert.doesNotMatch(right, /todo|Todos/i);
  assert.match(composer, /onSubmit/);
  assert.match(composer, /value=\{prompt\}/);
  assert.match(runtimeClient, /new EventSource\("\/api\/events"\)/);
  assert.match(runtimeHook, /fetchRuntimeSnapshot/);
  assert.match(runtimeHook, /submitRuntimeTurn/);
  assert.match(runtimeTimeline, /applyRuntimeEvent/);
  assert.match(runtimeTimeline, /text_delta/);
  assert.match(css, /grid-template-areas: "sidebar main right" "sidebar footer right"/);
  assert.match(css, /height: var\(--header-height\)/);
  assert.match(css, /border-right: 1px solid var\(--color-border-subtle\)/);
  assert.match(css, /--trees-selected-bg-override: var\(--color-accent-soft\)/);
  assert.match(css, /\.timeline-aux/);
  assert.match(css, /\.runtime-pill\.connected/);
  assert.match(css, /\.message-body[\s\S]*font-weight: var\(--font-weight-content-normal\)/);
  assert.match(css, /\.composer textarea[\s\S]*font-weight: var\(--font-weight-content-normal\)/);
  assert.match(css, /\.terminal-block pre/);
  assert.match(css, /max-width: 920px/);
  assert.match(css, /data-left-open="true"/);
  assert.match(tokens, /@layer theme\.palette, theme\.semantic, theme\.component, base/);
  assert.match(tokens, /--palette-blue-600:/);
  assert.match(tokens, /--color-bg-surface:/);
  assert.match(tokens, /--shell-left-width:/);
  assert.doesNotMatch(tokens, /--bg:|--line:|--accent:/);
  assert.match(tokens, /--font-sans:/);
  assert.match(tokens, /--font-weight-ui-normal: 500/);
  assert.match(tokens, /--font-weight-content-normal: 600/);
  assert.match(tokens, /--font-weight-ui-medium: 600/);
  assert.match(tokens, /--font-weight-ui-strong: 700/);
  assert.match(tokens, /font-weight: var\(--font-weight-ui-normal\)/);
  assert.match(tokens, /"Segoe UI Variable Text"/);
  assert.match(tokens, /DengXian/);
  assert.match(tokens, /"等线"/);
  assert.match(tokens, /DengXian[\s\S]*"Microsoft YaHei UI"/);
  assert.doesNotMatch(tokens, /PingFang SC|Noto Sans CJK|Source Han Sans|微软雅黑/i);
  assert.doesNotMatch(css, /todo/i);
  assert.match(server, /text\/event-stream/);
  assert.equal(resolveStaticPath(root, "/..%2fpackage.json"), null);
  assert.ok(resolveStaticPath(root, "/")?.endsWith(join("web-ui", "index.html")));
  console.log("  PASS");
}
